package myscala.util


object Email {

    private val EmailRegex: String = "(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])"

    def unapplySeq[T](arg: T)(implicit transform: T => String): Option[(String, Seq[String])] = arg match {
        case _ if isValid(arg) =>
            val parts = arg.split("@")
            Option((parts(0), parts(1).split(raw"\.")))
        case _ => None
    }

    def isValid[T](s: T)(implicit transform: T => String): Boolean = s match {
        case null => false
        case _ if s.matches(Email.EmailRegex) => true
        case _ => false
    }

}
