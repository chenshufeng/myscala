package myscala.util

import myscala._

object Email {

    private final val EmailRegex = "(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])".r

    def unapply(arg: String): Option[(String, String)] = arg match {
        case x if x.isNullOrBlank => None
        case x if isValid(arg) =>
            val array = x.split("@")
            if (array.length == 2) Option(array(0), array(1)) else None
        case _ => None
    }

    def isValid[S <: CharSequence](s: S): Boolean = s match {
        case null => false
        case EmailRegex() => true
        case _ => false
    }

}

object Domain {

    def unapplySeq(domain: String): Option[Seq[String]] = Option(domain.split(raw"\."))

}
